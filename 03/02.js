// Write a JavaScript program to reverse the elements of a given array of integers.

function reverseArray(arr = []) {
    return arr.reverse()
}

let result1 = reverseArray([10, 24, 200, 8, 40, 90])
let result2 = reverseArray([10, 20, 30, 40, 50])
let result3 = reverseArray([25, 60, 2, 5, 32])

console.log(result1)
console.log(result2)
console.log(result3)
